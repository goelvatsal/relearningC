/* This program adds two integers
and displays the results */


#include <stdio.h>

int main(void) {
    // Declare variables and assign values
    int value1 = 50, value2 = 25;
    int sum = value1 + value2;

    // Displays the result
    printf("The sum of %d and %d is %d.\n", value1, value2, sum);
}